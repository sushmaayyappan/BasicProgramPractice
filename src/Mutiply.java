public class Mutiply {
    private int Result;
    public int multiplyValue(int a,int b){
        Result=a*b;
        return Result;
    }
    public int multiplyValue(int a,int b,int c){
        Result=a*b*c;
        return Result;
    }
    public double multiplyValue(double a,double b){
        return a*b;
    }
}
